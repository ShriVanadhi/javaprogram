package javaapplication1;
 
public class Forloopprogram {
    public static void main(String[] args){
            //intializing i is 0 and incrementing till 5 using for loop
        for(int i=0;i<5;i++){
        //Printing the value of i until the increment continues
            System.out.println("value of i is" + " " + i);
       }
    }
    
}
