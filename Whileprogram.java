package javaapplication1;

public class Whileprogram {
    public static void main(String[] args){
        /*
        *Intializing the variable, value = 0 
        */
        int value = 0;
        /*
        *while loop executes till the value less than 10
        */
        while(value<10){
            /*
        *this is a comment
        */
            System.out.println("test" + " " + value);
            /*
        *value increases by 1 at each execution
        */
        value = value+1;
    }
  }
}
